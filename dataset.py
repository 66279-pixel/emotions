def build_dataset(target_per_class: int = 95, random_state: int = RANDOM_STATE) -> pd.DataFrame:
    """Builds the balanced, labeled emotion dataset as a DataFrame with text/label columns."""
    random.seed(random_state)
    all_rows = []
    for label, base in [
        ("joy", joy_base), ("sadness", sadness_base), ("anger", anger_base),
        ("fear", fear_base), ("surprise", surprise_base), ("neutral", neutral_base)
    ]:
        all_rows.extend(augment(base, label, target_per_class))
    random.shuffle(all_rows)
    return pd.DataFrame(all_rows, columns=["text", "label"])



# Light syntactic augmentation: vary the sentence opener / subject without
# introducing cross-class shortcut phrases. Applied evenly to all classes.
# ---------------------------------------------------------------------------

openers = ["", "Honestly, ", "To be fair, ", "Looking back, ", "These days, ",
           "Right now, ", "If I'm being honest, ", "Lately, "]

def capitalize_first(s):
    return s[0].upper() + s[1:] if s else s

def augment(sentences, label, n_target):
    """Combine base sentences with light opener variation to reach n_target rows."""
    rows = []
    for s in sentences:
        rows.append((capitalize_first(s) + ".", label))
    # add opener-varied duplicates of a subset until we reach n_target
    idx = 0
    pool = sentences.copy()
    random.shuffle(pool)
    while len(rows) < n_target:
        s = pool[idx % len(pool)]
        opener = random.choice(openers[1:])  # skip empty opener (already covered)
        new_sentence = opener + s
        rows.append((capitalize_first(new_sentence) + ".", label))
        idx += 1
    return rows[:n_target]

